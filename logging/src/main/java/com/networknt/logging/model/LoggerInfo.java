package com.networknt.logging.model;

public class LoggerInfo {
    private String name;
    private String level;

    public LoggerInfo() {

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }


}
